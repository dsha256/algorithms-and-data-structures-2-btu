void chop(char s[], int lim) {
    int i;
    /*
     * პირველ რიგში ვპოულობთ სტრიგის დასასრულელზე მიმანიშნებელ სიმბოლოს
     *  ამასობაში i სტრინგში არსებულ სიმბოლოებს გაუტოლდა.
     * */
    for (i = 0; s[i] != '\0'; ++i) {
    }

    // ეხლა შევამცირებთ i-ს მნიშვნელობას ვაიტსფეისამდე
    for (i -= 1; s[i] == '\t' || s[i] == '\n' || s[i] == ' '; --i) {
    }
    s[i + 1] = '\0';
}